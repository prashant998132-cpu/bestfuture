export function getLinksByCategory(category: string): AILink[] {
  if (category === 'all') return JARVIS_LINKS.filter(l => l.isActive);
  return JARVIS_LINKS.filter(l => l.category === category && l.isActive);
}

export function searchLinks(query: string): AILink[] {
  const q = query.toLowerCase();
  return JARVIS_LINKS.filter(l =>
    l.isActive &&
    (
      l.name.toLowerCase().includes(q) ||
      l.description.toLowerCase().includes(q) ||
      l.tags.some(t => t.toLowerCase().includes(q)) ||
      l.category.toLowerCase().includes(q)
    )
  );
}

export function getLinkById(id: string): AILink | undefined {
  return JARVIS_LINKS.find(l => l.id === id);
}

